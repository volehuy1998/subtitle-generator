# RATE LIMIT ALLOWLIST BYPASS
# ======================================================================


class TestRateLimitAllowlistBypass:
    """Test that allowlisted IPs bypass rate limiting entirely."""

    def test_allowlisted_ip_bypasses_rate_limit(self):
        """Allowlisted IP with exhausted bucket still gets 200."""
        ip = "10.99.99.99"
        key = f"ip:{ip}"
        try:
            add_to_allowlist(ip)
            for _ in range(65):
                check_rate_limit(key, limit=60, window=60)
            from app.services.rate_limiter import is_ip_allowed

            assert is_ip_allowed(ip) is True
        finally:
            remove_from_allowlist(ip)
            _buckets.pop(key, None)

    def test_non_allowlisted_ip_not_bypassed(self):
        """Non-allowlisted IP is subject to rate limiting."""
        from app.services.rate_limiter import is_ip_allowed

        assert is_ip_allowed("10.99.99.100") is None
