# RATE LIMIT HEADERS ON SUCCESS
# ======================================================================


class TestRateLimitHeadersOnSuccess:
    """Test rate limit headers generated by the rate limiter service."""

    def test_success_info_produces_limit_header(self):
        """get_rate_limit_headers includes X-RateLimit-Limit for allowed requests."""
        key = "test_header_limit"
        try:
            _, info = check_rate_limit(key, limit=60, window=60)
            headers = get_rate_limit_headers(info)
            assert "X-RateLimit-Limit" in headers
            assert headers["X-RateLimit-Limit"] == "60"
        finally:
            _buckets.pop(key, None)

    def test_success_info_produces_remaining_header(self):
        """get_rate_limit_headers includes X-RateLimit-Remaining."""
        key = "test_header_remaining"
        try:
            _, info = check_rate_limit(key, limit=60, window=60)
            headers = get_rate_limit_headers(info)
            assert "X-RateLimit-Remaining" in headers
            assert int(headers["X-RateLimit-Remaining"]) == 59
        finally:
            _buckets.pop(key, None)

    def test_remaining_decrements_on_successive_requests(self):
        """Remaining value decrements on successive check_rate_limit calls."""
        key = "test_header_decrement"
        try:
            _, info1 = check_rate_limit(key, limit=60, window=60)
            _, info2 = check_rate_limit(key, limit=60, window=60)
            h1 = get_rate_limit_headers(info1)
            h2 = get_rate_limit_headers(info2)
            assert int(h2["X-RateLimit-Remaining"]) < int(h1["X-RateLimit-Remaining"])
        finally:
            _buckets.pop(key, None)

    def test_limit_header_reflects_configured_value(self):
        """X-RateLimit-Limit reflects the configured limit value."""
        key = "test_header_configured"
        try:
            _, info = check_rate_limit(key, limit=100, window=60)
            headers = get_rate_limit_headers(info)
            assert headers["X-RateLimit-Limit"] == "100"
        finally:
            _buckets.pop(key, None)


