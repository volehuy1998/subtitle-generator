# RATE LIMIT KEY ISOLATION
# ======================================================================


class TestRateLimitKeyIsolation:
    """Test that upload and general rate limit buckets are isolated."""

    def test_upload_exhaustion_does_not_affect_general(self):
        """Exhausting upload requests doesn't affect general IP bucket."""
        upload_key = "upload:testclient"
        general_key = "ip:testclient"
        try:
            for _ in range(10):
                check_rate_limit(upload_key, limit=5, window=60)
            allowed, info = check_rate_limit(general_key, limit=60, window=60)
            assert allowed is True
        finally:
            _buckets.pop(upload_key, None)
            _buckets.pop(general_key, None)

    def test_general_exhaustion_does_not_affect_upload(self):
        """Exhausting general bucket doesn't affect upload bucket."""
        upload_key = "upload:keyiso2"
        general_key = "ip:keyiso2"
        try:
            for _ in range(65):
                check_rate_limit(general_key, limit=60, window=60)
            allowed, info = check_rate_limit(upload_key, limit=5, window=60)
            assert allowed is True
        finally:
            _buckets.pop(upload_key, None)
            _buckets.pop(general_key, None)

    def test_different_ips_have_separate_buckets(self):
        """Different IP addresses use separate rate limit buckets."""
        key1 = "ip:10.0.0.1"
        key2 = "ip:10.0.0.2"
        try:
            for _ in range(65):
                check_rate_limit(key1, limit=60, window=60)
            allowed, _ = check_rate_limit(key2, limit=60, window=60)
            assert allowed is True
        finally:
            _buckets.pop(key1, None)
            _buckets.pop(key2, None)


